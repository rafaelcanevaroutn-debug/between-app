export function generateEcosystem({ nombre, nicho }) {
  const base = ECOSYSTEM[nicho] || ECOSYSTEM['Marca Personal']
  const slug = nombre.toLowerCase().replace(/\s+/g, '').replace(/[^a-z0-9]/g, '').slice(0, 12)
  const firstName = nombre.split(' ')[0]

  return base.map((canal, i) => {
    const sufijo = canal.sufijo.toLowerCase()
    return {
      handle: i === 0 ? `@${slug}` : `@${slug}${sufijo}`,
      nombre: i === 0 ? firstName : `${firstName} ${canal.sufijo}`,
      bio: canal.bio,
      videos: canal.videos,
      enfoque: canal.enfoque,
    }
  })
}
